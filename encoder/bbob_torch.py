"""Differentiable torch implementation of the BBOB (COCO 2009 noiseless) suite.

Mirrors the interface of `encoder/cec2017_torch.py` (`CEC2017Torch`) so it can be
dropped into `AugmentedCEC2017`/the task pool: `BBOBTorch(fid, D, device)` is
callable on coords of shape (B, D) (float64), returns (B,) with gradients
flowing — required because TersQ BPTT differentiates through the objective.

Instance parameters (x_opt, f_opt, R, Q) are generated by a FAITHFUL port of
coco's `suite_bbob_legacy_code.c` RNG (verified bit-exact vs cocoex on f1, see
the __main__ validation harness). The RNG runs once at construction (numpy);
the __call__ math is pure torch and differentiable.

Reference: Hansen et al. 2009, "Real-Parameter Black-Box Optimization
Benchmarking 2009: Noiseless Functions Definitions".
"""
import math

import numpy as np
import torch

PI = math.pi


# ===========================================================================
# coco BBOB-2009 RNG (faithful port of suite_bbob_legacy_code.c) — numpy/host
# ===========================================================================
def bbob_unif(N, inseed):
    """Verbatim port of bbob2009_unif. Returns a list[float] of length N."""
    inseed = int(inseed)
    if inseed < 0:
        inseed = -inseed
    if inseed < 1:
        inseed = 1
    aktseed = inseed
    rgrand = [0] * 32
    for i in range(39, -1, -1):
        tmp = int(math.floor(aktseed / 127773.0))
        aktseed = 16807 * (aktseed - tmp * 127773) - 2836 * tmp
        if aktseed < 0:
            aktseed += 2147483647
        if i < 32:
            rgrand[i] = aktseed
    aktrand = rgrand[0]
    r = [0.0] * N
    for i in range(N):
        tmp = int(math.floor(aktseed / 127773.0))
        aktseed = 16807 * (aktseed - tmp * 127773) - 2836 * tmp
        if aktseed < 0:
            aktseed += 2147483647
        tmp = int(math.floor(aktrand / 67108865.0))
        aktrand = rgrand[tmp]
        rgrand[tmp] = aktseed
        r[i] = aktrand / 2.147483647e9
        if r[i] == 0.0:
            r[i] = 1e-99
    return r


def bbob_gauss(N, seed):
    """Verbatim port of bbob2009_gauss (Box-Muller). Returns list[float]."""
    u = bbob_unif(2 * N, seed)
    g = [0.0] * N
    for i in range(N):
        g[i] = math.sqrt(-2.0 * math.log(u[i])) * math.cos(2.0 * PI * u[N + i])
        if g[i] == 0.0:
            g[i] = 1e-99
    return g


def compute_xopt(seed, D):
    x = bbob_unif(D, seed)
    for i in range(D):
        x[i] = 8.0 * math.floor(1e4 * x[i]) / 1e4 - 4.0
        if x[i] == 0.0:
            x[i] = -1e-5
    return np.array(x, dtype=np.float64)


def _fmin(a, b):
    return a if a < b else b


def _fmax(a, b):
    return a if a > b else b


def _bround(x):
    return math.floor(x + 0.5)


def compute_fopt(function, instance):
    if function == 4:
        rseed = 3
    elif function == 18:
        rseed = 17
    else:
        rseed = function
    rrseed = rseed + 10000 * instance
    gval = bbob_gauss(1, rrseed)[0]
    gval2 = bbob_gauss(1, rrseed + 1)[0]
    return _fmin(1000.0, _fmax(-1000.0, _bround(100.0 * 100.0 * gval / gval2) / 100.0))


def compute_rotation(seed, D):
    """Faithful port of bbob2009_compute_rotation. Returns (D, D) np.float64.

    coco fills B column-major from the gauss vector (reshape: B[i][j] =
    vector[j*D + i]) then Gram-Schmidt orthonormalizes the COLUMNS.
    """
    g = bbob_gauss(D * D, seed)
    B = np.empty((D, D), dtype=np.float64)
    for i in range(D):
        for j in range(D):
            B[i][j] = g[j * D + i]
    for i in range(D):
        for j in range(i):
            prod = float(np.dot(B[:, i], B[:, j]))
            B[:, i] -= prod * B[:, j]
        B[:, i] /= math.sqrt(float(np.dot(B[:, i], B[:, i])))
    return B


# ===========================================================================
# Differentiable transform primitives (torch, hot path)
# ===========================================================================
def t_osc(x):
    """T_osz oscillation transform (element-wise, differentiable)."""
    xabs = torch.abs(x)
    xhat = torch.where(x != 0, torch.log(torch.clamp(xabs, min=1e-300)),
                       torch.zeros_like(x))
    c1 = torch.where(x > 0, torch.full_like(x, 10.0), torch.full_like(x, 5.5))
    c2 = torch.where(x > 0, torch.full_like(x, 7.9), torch.full_like(x, 3.1))
    sgn = torch.sign(x)
    return sgn * torch.exp(xhat + 0.049 * (torch.sin(c1 * xhat) + torch.sin(c2 * xhat)))


def t_asy(x, beta, D):
    """T_asy^beta asymmetry transform (only positive coords)."""
    i = torch.arange(D, device=x.device, dtype=x.dtype)
    expo = 1.0 + beta * (i / max(D - 1, 1)) * torch.sqrt(torch.clamp(x, min=0.0))
    return torch.where(x > 0, x ** expo, x)


def lambda_diag(alpha, D, device, dtype):
    """Diagonal of Lambda^alpha: lambda_ii = alpha^(0.5 * i/(D-1))."""
    i = torch.arange(D, device=device, dtype=dtype)
    return alpha ** (0.5 * i / max(D - 1, 1))


def f_pen(x):
    """Boundary penalty sum(max(0,|x|-5)^2)."""
    return torch.sum(torch.clamp(torch.abs(x) - 5.0, min=0.0) ** 2, dim=1)


# ===========================================================================
# BBOBTorch — one (fid, D, instance) function, differentiable
# ===========================================================================
N_FUNCS = 24
DIMS_DEFAULT = (10,)


class BBOBTorch:
    def __init__(self, fid, D, device, instance=1, dtype=torch.float64):
        assert 1 <= fid <= N_FUNCS, f"BBOB fid must be 1..24, got {fid}"
        self.fid = int(fid)
        self.D = int(D)
        self.device = device
        self.dtype = dtype
        self.instance = int(instance)
        self.f_opt = float(compute_fopt(self.fid, self.instance))
        self.rseed = self.fid + 10000 * self.instance
        # x_opt (most functions); special-cased per function where needed
        xopt = compute_xopt(self.rseed, self.D)
        self.x_opt = torch.tensor(xopt, device=device, dtype=dtype)
        # Drop-in compatibility with the AugmentedCEC2017 / task-pool interface:
        # the trainer reads `.f_optimal` (known optimum value, for gap/hit) and
        # `.shift` (optimum LOCATION, for the disentangle/LUPI x_star — see
        # train_distributed._get_x_star, which uses .shift for plain fns).
        self.f_optimal = self.f_opt
        self.shift = self.x_opt
        # rotations (lazy per function): R = rot(rseed+1e6), Q = rot(rseed)
        self._R = None
        self._Q = None

    # -- rotation accessors (built on demand) --
    # coco convention (verified vs cocoex on f10): the PRIMARY rotation uses
    # seed rseed+1e6, the SECONDARY uses rseed. Two-rotation functions assign
    # rot1=R=rot(rseed+1e6), rot2=Q=rot(rseed) — validate per function vs cocoex.
    @property
    def R(self):
        if self._R is None:
            self._R = torch.tensor(compute_rotation(self.rseed + 1000000, self.D),
                                   device=self.device, dtype=self.dtype)
        return self._R

    @property
    def Q(self):
        if self._Q is None:
            self._Q = torch.tensor(compute_rotation(self.rseed, self.D),
                                   device=self.device, dtype=self.dtype)
        return self._Q

    def _rot(self, x, M):
        # x: (B, D); M: (D, D) acting as M @ (x)  -> (B, D)
        return x @ M.T

    # -- dispatch --
    def __call__(self, x):
        x = x.to(self.dtype)
        if x.dim() == 1:
            x = x.unsqueeze(0)
        fn = _REGISTRY.get(self.fid)
        if fn is None:
            raise NotImplementedError(f"BBOB f{self.fid} not yet implemented")
        return fn(self, x) + self.f_opt


# ---- function registry (filled below; workflow appends the rest) ----
_REGISTRY = {}


def _register(fid):
    def deco(fn):
        _REGISTRY[fid] = fn
        return fn
    return deco


# f1 — Sphere
@_register(1)
def _f1(self, x):
    z = x - self.x_opt
    return torch.sum(z * z, dim=1)


# f2 — Ellipsoidal separable: sum 10^(6 i/(D-1)) z_i^2 ; z = Tosz(x - xopt)
@_register(2)
def _f2(self, x):
    D = self.D
    z = t_osc(x - self.x_opt)
    i = torch.arange(D, device=x.device, dtype=x.dtype)
    coeff = 10.0 ** (6.0 * i / max(D - 1, 1))
    return torch.sum(coeff * z * z, dim=1)


# f10 — Ellipsoidal rotated: z = Tosz(R (x - xopt)); sum 10^(6 i/(D-1)) z_i^2
@_register(10)
def _f10(self, x):
    D = self.D
    z = t_osc(self._rot(x - self.x_opt, self.R))
    i = torch.arange(D, device=x.device, dtype=x.dtype)
    coeff = 10.0 ** (6.0 * i / max(D - 1, 1))
    return torch.sum(coeff * z * z, dim=1)




