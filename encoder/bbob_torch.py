# f23 — Katsuura
# z = M (x - xopt), M = R1 @ Lambda^100 @ R2 ; R1=rot(rseed+1e6)=R, R2=rot(rseed)=Q
# Lambda^100 diag = 100^(0.5 i/(D-1)) = lambda_diag(100,...). Boundary penalty
# f_pen is on the ORIGINAL x (coco transform_obj_penalize, factor 1.0).
@_register(23)
def _f23(self, x):
    D = self.D
    R1 = self.R          # rot(rseed + 1e6)
    R2 = self.Q          # rot(rseed)
    lam = lambda_diag(100.0, D, x.device, x.dtype)        # (D,)
    M = R1 @ (lam[:, None] * R2)                          # (D, D)
    z = (x - self.x_opt) @ M.T                            # (B, D)
    j = torch.arange(1, 33, device=x.device, dtype=x.dtype)   # 1..32
    two_j = 2.0 ** j                                      # (32,)
    zz = z.unsqueeze(-1) * two_j                          # (B, D, 32)
    tmp = torch.sum(torch.abs(zz - torch.round(zz)) / two_j, dim=-1)  # (B, D)
    i = torch.arange(D, device=x.device, dtype=x.dtype)
    per_dim = 1.0 + (i + 1.0) * tmp                       # (B, D)
    expo = 10.0 / (D ** 1.2)
    prod = torch.prod(per_dim ** expo, dim=1)             # (B,)
    raw = (10.0 / (D * D)) * (-1.0 + prod)
    return raw + f_pen(x)


# f24 — Lunacek bi-Rastrigin
# mu0=2.5, d=1, s=1-0.5/(sqrt(D+20)-4.1), mu1=-sqrt((mu0^2-d)/s)
# NON-standard xopt: sign = sign(bbob_gauss(D, rseed)); only its sign enters x_hat.
# x_hat = 2*sign*x; tmpvect = Lambda^100 @ R2 @ (x_hat-mu0); z = R1 @ tmpvect.
# R1=rot(rseed+1e6)=R, R2=rot(rseed)=Q. sum1/sum2 use x_hat; sum3=cos(2 pi z).
@_register(24)
def _f24(self, x):
    D = self.D
    mu0 = 2.5
    d = 1.0
    s = 1.0 - 0.5 / (math.sqrt(D + 20.0) - 4.1)
    mu1 = -math.sqrt((mu0 * mu0 - d) / s)
    if not hasattr(self, "_f24_sign"):
        g = bbob_gauss(D, self.rseed)
        sign = np.where(np.array(g) < 0.0, -1.0, 1.0)
        self._f24_sign = torch.tensor(sign, device=x.device, dtype=x.dtype)
    sign = self._f24_sign
    R1 = self.R          # rot(rseed + 1e6)
    R2 = self.Q          # rot(rseed)
    lam = lambda_diag(100.0, D, x.device, x.dtype)        # (D,) = 100^(0.5 i/(D-1))
    x_hat = 2.0 * sign * x                                # (B, D)
    diff0 = x_hat - mu0                                   # (B, D)
    tmpvect = (diff0 @ R2.T) * lam                        # (B, D)
    z = tmpvect @ R1.T                                    # (B, D)
    sum1 = torch.sum(diff0 * diff0, dim=1)
    diff1 = x_hat - mu1
    sum2 = torch.sum(diff1 * diff1, dim=1)
    sum3 = torch.sum(torch.cos(2.0 * math.pi * z), dim=1)
    bi = torch.minimum(sum1, d * D + s * sum2)
    return bi + 10.0 * (D - sum3) + 1e4 * f_pen(x)


