# f18 — Schaffers F7 ill-conditioned (conditioning=1000, penalty_scale=10).
# coco passes rseed_17 = 17 + 10000*instance (NOT 18) to the allocator, so xopt
# AND both rotations use the f17 seed. f_opt is already special-cased in
# compute_fopt (function==18 -> rseed=17), so __call__ adds the correct f_opt;
# only xopt/R/Q need the rseed override here.
@_register(18)
def _f18(self, x):
    if getattr(self, "_f18_xopt", None) is None:
        rseed17 = 17 + 10000 * self.instance
        self._f18_xopt = torch.tensor(
            compute_xopt(rseed17, self.D), device=x.device, dtype=x.dtype)
        self._f18_R = torch.tensor(
            compute_rotation(rseed17 + 1000000, self.D), device=x.device, dtype=x.dtype)
        self._f18_Q = torch.tensor(
            compute_rotation(rseed17, self.D), device=x.device, dtype=x.dtype)
    return _schaffers(self, x, conditioning=1000.0, penalty_scale=10.0,
                      x_opt=self._f18_xopt, R=self._f18_R, Q=self._f18_Q)


# f19 — Composite Griewank-Rosenbrock F8F2 (facftrue=10).
# coco transform stack (f_griewank_rosenbrock_bbob_problem_allocate):
#   scales = max(1, sqrt(D)/8); M = scales * rot1 (rot1 = rotation(rseed) = self.Q)
#   allocate order: shift(-0.5) then affine(M) -> affine is OUTER (applied FIRST):
#       z = M @ x = scales * (rot1 @ x)
#       z = z + 0.5            # transform_vars_shift with shift_i=-0.5 -> z - (-0.5)
#   raw: for i: c1=z_i^2 - z_{i+1}; c2=1 - z_i; t=100 c1^2 + c2^2; add t/4000 - cos t
#   out = facftrue + facftrue * sum/(D-1)   (facftrue=10). f_opt added by __call__.
# NOTE: x_opt/f_opt for f19 use the standard rseed=19; xopt isn't needed by the math
# (the affine has zero bias), so self.x_opt is unused here.
@_register(19)
def _f19(self, x):
    D = self.D
    scales = max(1.0, math.sqrt(D) / 8.0)
    z = scales * self._rot(x, self.Q)  # self.Q = rotation(rseed) = coco rot1
    z = z + 0.5
    c1 = z[:, :-1] ** 2 - z[:, 1:]
    c2 = 1.0 - z[:, :-1]
    tmp = 100.0 * c1 * c1 + c2 * c2
    inner = torch.sum(tmp / 4000.0 - torch.cos(tmp), dim=1)
    facftrue = 10.0
    return facftrue + facftrue * inner / (D - 1.0)


# f20 — Schwefel
@_register(20)
def _f20(self, x):
    D = self.D
    dev, dt = x.device, x.dtype
    if getattr(self, "_f20_params", None) is None:
        u = bbob_unif(D, self.rseed)
        xopt = np.array([(-1.0 if u[i] < 0.5 else 1.0) * 0.5 * 4.2096874637
                         for i in range(D)], dtype=np.float64)
        # x_hat sign vector uses the SAME unif(rseed) draw
        sgn = np.array([-1.0 if u[i] < 0.5 else 1.0 for i in range(D)],
                       dtype=np.float64)
        self._f20_params = (
            torch.tensor(np.abs(xopt), device=dev, dtype=dt),   # |xopt|
            torch.tensor(sgn, device=dev, dtype=dt),
        )
    absxopt, sgn = self._f20_params

    # COCO transform chain (outermost->innermost as applied to the input):
    #   x_hat -> scale(2) -> z_hat -> shift(2|xopt|) -> cond(10)
    #   -> shift(-2|xopt|) -> scale(100) -> raw Schwefel
    x2 = 2.0 * (sgn * x)                                  # x_hat then scale(2)
    z0 = x2[:, :1]
    z_rest = x2[:, 1:] + 0.25 * (x2[:, :-1] - 2.0 * absxopt[:-1].unsqueeze(0))
    z = torch.cat([z0, z_rest], dim=1) if D > 1 else z0  # z_hat
    z = z - (2.0 * absxopt).unsqueeze(0)                  # shift(tmp2=2|xopt|)
    z = lambda_diag(10.0, D, dev, dt).unsqueeze(0) * z    # conditioning(10)
    z = z + (2.0 * absxopt).unsqueeze(0)                  # shift(tmp1=-2|xopt|)
    z = 100.0 * z                                         # scale(100)

    penalty = torch.sum(torch.clamp(torch.abs(z) - 500.0, min=0.0) ** 2, dim=1)
    # sqrt(|z|) grad is +inf at z=0; clamp the radicand (forward unchanged).
    s = torch.sum(z * torch.sin(torch.sqrt(torch.clamp(torch.abs(z), min=1e-300))),
                  dim=1)
    return 0.01 * (penalty + 418.9828872724339 - s / D)


# ---- shared Gallagher helpers (module-level; used by f21 and f22) ----
def _gallagher_params(self, num_peaks):
    """Build (rotation, arr_scales(P,D), x_local(P,D), peak_values(P)) for the
    BBOB Gallagher problem. Faithful port of f_gallagher_bbob_problem_allocate."""
    D = self.D
    dev, dt = self.device, self.dtype
    maxcondition = 1000.0
    maxcondition1 = 1000.0
    fitvalues = (1.1, 9.1)
    if num_peaks == 101:
        maxcondition1 = math.sqrt(maxcondition1)
        b, c = 10.0, 5.0
    elif num_peaks == 21:
        b, c = 9.8, 4.9
    else:
        raise ValueError(f"unsupported number_of_peaks {num_peaks}")

    rotation = compute_rotation(self.rseed, D)  # seed = rseed (== self.Q)

    # peak conditions + values via a sort-permutation of (num_peaks-1) unifs
    rn = bbob_unif(num_peaks - 1, self.rseed)
    perm = sorted(range(num_peaks - 1), key=lambda i: rn[i])  # qsort by value
    arr_cond = np.empty(num_peaks, dtype=np.float64)
    peak_values = np.empty(num_peaks, dtype=np.float64)
    arr_cond[0] = maxcondition1
    peak_values[0] = 10.0
    for i in range(1, num_peaks):
        arr_cond[i] = maxcondition ** (perm[i - 1] / (num_peaks - 2))
        peak_values[i] = ((i - 1) / (num_peaks - 2)
                          * (fitvalues[1] - fitvalues[0]) + fitvalues[0])

    # per-peak diagonal scales Lambda^alpha_i, dim-permuted
    arr_scales = np.empty((num_peaks, D), dtype=np.float64)
    for i in range(num_peaks):
        rnj = bbob_unif(D, self.rseed + 1000 * i)
        permj = sorted(range(D), key=lambda j: rnj[j])
        for j in range(D):
            arr_scales[i, j] = arr_cond[i] ** (permj[j] / (D - 1) - 0.5)

    # peak positions (rotated), peak 0 scaled by 0.8
    rn2 = bbob_unif(D * num_peaks, self.rseed)
    x_local = np.zeros((num_peaks, D), dtype=np.float64)  # [peak][dim]
    for i in range(D):
        for j in range(num_peaks):
            s = 0.0
            for k in range(D):
                s += rotation[i, k] * (b * rn2[j * D + k] - c)
            x_local[j, i] = s * 0.8 if j == 0 else s

    return (
        torch.tensor(rotation, device=dev, dtype=dt),
        torch.tensor(arr_scales, device=dev, dtype=dt),     # (P, D)
        torch.tensor(x_local, device=dev, dtype=dt),        # (P, D)
        torch.tensor(peak_values, device=dev, dtype=dt),    # (P,)
    )


def _gallagher_raw(self, x, num_peaks, cache_attr):
    D = self.D
    params = getattr(self, cache_attr, None)
    if params is None:
        params = _gallagher_params(self, num_peaks)
        setattr(self, cache_attr, params)
    rotation, arr_scales, x_local, peak_values = params

    pen = torch.sum(torch.clamp(torch.abs(x) - 5.0, min=0.0) ** 2, dim=1)
    tmx = x @ rotation.T                                       # (B, D)
    diff = tmx.unsqueeze(1) - x_local.unsqueeze(0)            # (B, P, D)
    quad = torch.sum(arr_scales.unsqueeze(0) * diff * diff, dim=2)  # (B, P)
    vals = peak_values.unsqueeze(0) * torch.exp(-0.5 / D * quad)
    f = 10.0 - torch.max(vals, dim=1).values                 # (B,)

    a = 0.1
    pos, neg = f > 0, f < 0
    lt_p = torch.log(torch.clamp(f, min=1e-300)) / a
    ft_p = torch.pow(torch.exp(lt_p + 0.49 * (torch.sin(lt_p)
                     + torch.sin(0.79 * lt_p))), a)
    lt_n = torch.log(torch.clamp(-f, min=1e-300)) / a
    ft_n = -torch.pow(torch.exp(lt_n + 0.49 * (torch.sin(0.55 * lt_n)
                      + torch.sin(0.31 * lt_n))), a)
    f_true = torch.where(pos, ft_p, torch.where(neg, ft_n, f))
    return f_true * f_true + pen


