# f3 — Rastrigin separable: z = Lambda^10 Tasy^0.2 Tosz(x - xopt)
@_register(3)
def _f3(self, x):
    D = self.D
    z = t_osc(x - self.x_opt)
    z = t_asy(z, 0.2, D)
    z = lambda_diag(10.0, D, x.device, x.dtype) * z
    s1 = torch.sum(torch.cos(2.0 * PI * z), dim=1)
    s2 = torch.sum(z * z, dim=1)
    return 10.0 * (D - s1) + s2


# f4 — Bueche-Rastrigin. coco rseed=3 for xopt/fopt; even-indexed xopt coords
# are abs'd; brs scaling = Lambda^10 with a conditional x10 on positive even
# coords; boundary penalty 100*f_pen.
@_register(4)
def _f4(self, x):
    D = self.D
    if getattr(self, "_f4_xopt", None) is None:
        # coco f4 uses rseed = 3 + 10000*instance for x_opt (NOT fid=4)
        xo = compute_xopt(3 + 10000 * self.instance, D)
        xo = torch.tensor(xo, device=x.device, dtype=x.dtype)
        idx = torch.arange(D, device=x.device)
        xo = torch.where(idx % 2 == 0, torch.abs(xo), xo)
        self._f4_xopt = xo
    z = t_osc(x - self._f4_xopt)
    i = torch.arange(D, device=x.device, dtype=x.dtype)
    base = 10.0 ** (0.5 * i / max(D - 1, 1))
    even = (i.long() % 2 == 0)
    mult = torch.where((z > 0) & even, torch.full_like(z, 10.0),
                       torch.ones_like(z))
    z = base * mult * z
    s1 = torch.sum(torch.cos(2.0 * PI * z), dim=1)
    s2 = torch.sum(z * z, dim=1)
    return 10.0 * (D - s1) + s2 + 100.0 * f_pen(x)


# f5 — Linear Slope: xopt_i = 5*sign in {-5,+5}; s_i = sign(xopt_i)*10^(i/(D-1));
# z_i = x_i if x_i*xopt_i < 25 else xopt_i; f = sum(5|s_i| - s_i z_i).
@_register(5)
def _f5(self, x):
    D = self.D
    if getattr(self, "_f5_params", None) is None:
        sign = torch.sign(self.x_opt)
        xopt5 = 5.0 * sign
        i = torch.arange(D, device=x.device, dtype=x.dtype)
        s = sign * (10.0 ** (i / max(D - 1, 1)))
        self._f5_params = (xopt5, s)
    xopt5, s = self._f5_params
    z = torch.where(x * xopt5 < 25.0, x, xopt5)
    return torch.sum(5.0 * torch.abs(s) - s * z, dim=1)


# f6 — Attractive Sector: M = R Lambda^10 Q; z = M(x - xopt);
#      raw = sum_i s_i z_i^2, s_i = 100^2 if xopt_i*z_i>0 else 1;
#      obj = Tosz(raw)^0.9  (oscillate then power 0.9; raw>=0 -> t_osc exact)
@_register(6)
def _f6(self, x):
    D = self.D
    lam = lambda_diag(10.0, D, x.device, x.dtype)          # Lambda^10 diagonal
    xc = x - self.x_opt
    z = self._rot(lam * self._rot(xc, self.Q), self.R)      # R @ (Lambda * (Q @ (x-xopt)))
    s = torch.where(self.x_opt * z > 0.0,
                    torch.full_like(z, 100.0 * 100.0),
                    torch.ones_like(z))
    raw = torch.sum(s * z * z, dim=1)
    return t_osc(raw) ** 0.9


# f7 — Step Ellipsoidal (monolithic coco function):
#   x_tmp = Lambda^10 * (Q (x - xopt)); x1 = x_tmp[0]
#   round-step (coco floor(.+0.5)): |x_tmp|>0.5 -> floor(x_tmp+0.5) else floor(10 x_tmp+0.5)/10
#   xx = R x_round; result = 0.1*max(|x1|*1e-4, sum 100^(i/(D-1)) xx_i^2) + penalty (scale 1.0)
@_register(7)
def _f7(self, x):
    D = self.D
    cond = 100.0
    lam = lambda_diag(10.0, D, x.device, x.dtype)          # sqrt(100^(i/(D-1)))
    xt = lam * self._rot(x - self.x_opt, self.Q)           # Lambda * (Q @ (x-xopt))
    x1 = xt[:, 0]
    big = torch.floor(xt + 0.5)                            # coco_double_round = floor(.+0.5)
    small = torch.floor(10.0 * xt + 0.5) / 10.0
    xr = torch.where(torch.abs(xt) > 0.5, big, small)
    xx = self._rot(xr, self.R)                             # R @ x_round
    i = torch.arange(D, device=x.device, dtype=x.dtype)
    coeff = cond ** (i / max(D - 1, 1))
    quad = torch.sum(coeff * xx * xx, dim=1)
    inner = 0.1 * torch.maximum(torch.abs(x1) * 1.0e-4, quad)
    return inner + f_pen(x)                                # penalty_scale = 1.0


# --- module-level helper for f8 (lazy 0.75-scaled xopt cache) ---
def _rosen_xopt8(self):
    if getattr(self, "_f8_xopt", None) is None:
        xo = compute_xopt(self.rseed, self.D) * 0.75
        self._f8_xopt = torch.tensor(xo, device=self.device, dtype=self.dtype)
    return self._f8_xopt


# f8 — Rosenbrock (separable): z = factor*(x - 0.75*xopt) + 1; factor=max(1,sqrt(D)/8)
#      raw = 100*sum (z_i^2 - z_{i+1})^2 + sum (z_i - 1)^2
@_register(8)
def _f8(self, x):
    D = self.D
    factor = max(1.0, math.sqrt(D) / 8.0)
    z = factor * (x - _rosen_xopt8(self)) + 1.0
    s1 = torch.sum((z[:, :-1] ** 2 - z[:, 1:]) ** 2, dim=1)
    s2 = torch.sum((z[:, :-1] - 1.0) ** 2, dim=1)
    return 100.0 * s1 + s2


# f9 — Rosenbrock rotated: z = factor*(Q @ x) + 0.5; factor=max(1,sqrt(D)/8)
#      NOTE: coco uses rotation(rseed) == self.Q here (NOT rseed+1e6/self.R); no xopt shift.
#      raw = 100*sum (z_i^2 - z_{i+1})^2 + sum (z_i - 1)^2
@_register(9)
def _f9(self, x):
    D = self.D
    factor = max(1.0, math.sqrt(D) / 8.0)
    z = factor * self._rot(x, self.Q) + 0.5
    s1 = torch.sum((z[:, :-1] ** 2 - z[:, 1:]) ** 2, dim=1)
    s2 = torch.sum((z[:, :-1] - 1.0) ** 2, dim=1)
    return 100.0 * s1 + s2


# f11 — Discus: z = Tosz(R(x - xopt)); 1e6 z_0^2 + sum_{i>=1} z_i^2
@_register(11)
def _f11(self, x):
    z = t_osc(self._rot(x - self.x_opt, self.R))
    return 1.0e6 * z[:, 0] ** 2 + torch.sum(z[:, 1:] ** 2, dim=1)


# f12 — Bent Cigar: SPECIAL x_opt uses seed rseed+1e6.
# z = R Tasy^0.5(R(x - xopt)); z_0^2 + 1e6 sum_{i>=1} z_i^2
@_register(12)
def _f12(self, x):
    if getattr(self, "_f12_xopt", None) is None:
        xo = compute_xopt(self.rseed + 1000000, self.D)
        self._f12_xopt = torch.tensor(xo, device=x.device, dtype=x.dtype)
    z = self._rot(x - self._f12_xopt, self.R)
    z = t_asy(z, 0.5, self.D)
    z = self._rot(z, self.R)
    return z[:, 0] ** 2 + 1.0e6 * torch.sum(z[:, 1:] ** 2, dim=1)


# f13 — Sharp Ridge: M = R1 @ Lambda^10 @ R2, z = M(x - xopt).
# R1=rot(rseed+1e6)=self.R, R2=rot(rseed)=self.Q.
# z_0^2 + 100 sqrt(sum_{i>=1} z_i^2)
@_register(13)
def _f13(self, x):
    D = self.D
    lam = lambda_diag(10.0, D, x.device, x.dtype)  # 10^(0.5 i/(D-1)) = sqrt(10)^(i/(D-1))
    y = self._rot(x - self.x_opt, self.Q)          # R2 (x - xopt)
    y = lam * y                                     # Lambda^10
    z = self._rot(y, self.R)                        # R1
    return z[:, 0] ** 2 + 100.0 * torch.sqrt(torch.sum(z[:, 1:] ** 2, dim=1))


# f14 — Different Powers: z = R(x - xopt); sqrt( sum |z_i|^(2 + 4 i/(D-1)) )
@_register(14)
def _f14(self, x):
    D = self.D
    z = self._rot(x - self.x_opt, self.R)
    i = torch.arange(D, device=x.device, dtype=x.dtype)
    expo = 2.0 + 4.0 * i / max(D - 1, 1)
    return torch.sqrt(torch.sum(torch.abs(z) ** expo, dim=1))


# f15 — Rastrigin rotated: z = R Lambda^0.5(alpha=10) Q T_asy^0.2( T_osz( R (x - xopt) ) )
# COCO allocate registers (inner->outer effect): shift(xopt), affine(rot1),
# oscillate, asymmetric(0.2), affine(M=rot1.Lambda^0.5.rot2). The outermost
# (last-registered) transform runs FIRST on input, so the data flow on x is:
#   x - xopt  ->  R(.)  ->  T_osz  ->  T_asy^0.2  ->  M(.)   [M = R.Lambda^0.5.Q]
# NOTE: T_osz is applied BEFORE T_asy (decorator order), not asy-then-osz.
# R = rot(rseed+1e6) = coco rot1, Q = rot(rseed) = coco rot2.
@_register(15)
def _f15(self, x):
    D = self.D
    lam = lambda_diag(10.0, D, x.device, x.dtype)   # Lambda^0.5, alpha=10
    y = self._rot(x - self.x_opt, self.R)           # R (x - xopt)
    y = t_osc(y)                                     # T_osz
    y = t_asy(y, 0.2, D)                             # T_asy^0.2
    # M @ y with M = R @ diag(lam) @ Q  ==  R( lam * (Q y) )
    z = self._rot(y, self.Q)                         # Q y
    z = z * lam                                      # Lambda^0.5 Q y
    z = self._rot(z, self.R)                         # R Lambda^0.5 Q y = M y
    sum1 = torch.sum(torch.cos(2.0 * PI * z), dim=1)
    sum2 = torch.sum(z * z, dim=1)
    return 10.0 * (D - sum1) + sum2


# f16 — Weierstrass: z = R Lambda^0.5(alpha=1/100) Q T_osz( R (x - xopt) )
# base = 10 * ( (1/D) sum_i sum_{k=0..11} a^k cos(2 pi b^k (z_i + 0.5)) - f0 )^3
#   with a=0.5, b=3, f0 = sum_k a^k cos(2 pi b^k * 0.5).
# objective penalty (transform_obj_penalize, outermost) = (10/D) * f_pen(raw x).
# COCO allocate registers (inner->outer effect): shift(xopt), affine(rot1),
# oscillate, affine(M=rot1.Lambda^0.5.rot2), penalize(10/D). Data flow on x:
#   x - xopt -> R(.) -> T_osz -> M(.)   ;  penalty added on the ORIGINAL x.
# No asymmetry. R = rot(rseed+1e6) = rot1, Q = rot(rseed) = rot2.
_F16_F0 = sum((0.5 ** k) * math.cos(2.0 * PI * (3.0 ** k) * 0.5) for k in range(12))

@_register(16)
def _f16(self, x):
    D = self.D
    lam = lambda_diag(0.01, D, x.device, x.dtype)   # Lambda^0.5, alpha=1/100 -> base 0.1
    y = self._rot(x - self.x_opt, self.R)           # R (x - xopt)
    y = t_osc(y)                                     # T_osz
    # M @ y with M = R @ diag(lam) @ Q
    z = self._rot(y, self.Q)                         # Q y
    z = z * lam                                      # Lambda^0.5 Q y
    z = self._rot(z, self.R)                         # R Lambda^0.5 Q y = M y
    k = torch.arange(12, device=x.device, dtype=x.dtype)
    ak = 0.5 ** k                                    # (12,)
    bk = 3.0 ** k                                    # (12,)
    terms = ak * torch.cos(2.0 * PI * bk * (z.unsqueeze(-1) + 0.5))  # (B, D, 12)
    s = torch.sum(terms, dim=(1, 2))                 # (B,)
    base = 10.0 * (s / D - _F16_F0) ** 3
    pen = (10.0 / D) * f_pen(x)
    return base + pen


def _schaffers(self, x, conditioning, penalty_scale, x_opt=None, R=None, Q=None):
    """Schaffers F7 core (shared by f17 and f18).

    coco transform stack (f_schaffers_bbob_problem_allocate), evaluated on x in
    this order (vars transforms apply in REVERSE of allocate order):
        z = x - xopt
        z = R @ z                     # R = rot1 = rotation(rseed+1e6)
        z = T_asy^0.5(z)
        z = Lambda * (Q @ z)          # Q = rot2 = rotation(rseed);
                                      #   Lambda_ii = conditioning^(0.5 i/(D-1))
    raw = (sum_i (z_i^2+z_{i+1}^2)^0.25 (1+sin(50 (z_i^2+z_{i+1}^2)^0.1)^2) /(D-1))^2
    out = raw + penalty_scale * f_pen(ORIGINAL x)   # penalty on untransformed x
    (f_opt added by __call__).
    """
    D = self.D
    if x_opt is None:
        x_opt, R, Q = self.x_opt, self.R, self.Q
    z = x - x_opt
    z = self._rot(z, R)
    z = t_asy(z, 0.5, D)
    z = self._rot(z, Q)
    lam = lambda_diag(conditioning, D, x.device, x.dtype)  # (D,)
    z = lam * z
    s = z[:, :-1] ** 2 + z[:, 1:] ** 2  # (B, D-1)
    term = s ** 0.25 * (1.0 + torch.sin(50.0 * s ** 0.1) ** 2)
    result = (torch.sum(term, dim=1) / (D - 1.0)) ** 2
    result = result + penalty_scale * f_pen(x)
    return result


